const illustrationStyles = style({
  width: 'full',
  aspectRatio: '3/2',
  objectFit: 'cover',
  userSelect: 'none',
  pointerEvents: 'none'
});

interface ComponentCardGridProps {
  items: ComponentCardItem[],
  ariaLabel?: string,
  size?: 'S' | 'M' | 'L',
  onAction?: (key: Key) => void,
  styles?: any
}

export function ComponentCardGrid({items, ariaLabel = 'Items', size = 'S', onAction, styles}: ComponentCardGridProps) {
  return (
    <CardView aria-label={ariaLabel} onAction={onAction} styles={styles}>
      <Collection items={items}>
        {(item) => {
          let IllustrationComponent = componentIllustrations[item.name];
          return (
            <Card key={item.id} id={item.id} href={item.href} size={size}>
              {IllustrationComponent && (
                <CardPreview>
                  <IllustrationComponent
                    aria-hidden="true"
                    // @ts-ignore
                    className={illustrationStyles} />
                </CardPreview>
              )}
              <Content>
                <Text slot="title">{item.name}</Text>
              </Content>
            </Card>
          );
        }}
      </Collection>
    </CardView>
  );
}

export default ComponentCardGrid;


